num1  = int(input("Enter the 1st number :"))
oparetor = input("Enter the oparetor :")
num2 = int(input("Enter the 2nd number :"))

if oparetor == "+":
    print(num1+num2)
elif oparetor == "-":
    print(num1-num2)
elif oparetor == "*":
    print(num1*num2)
elif oparetor == "/":
    print(num1/num2)
elif oparetor == "%":
    print(num1%num2)
elif oparetor == "**":
    print(num1**num2)
elif oparetor == "//":
    print(num1//num2)        
else:
    print("Invert oparetor")
    







